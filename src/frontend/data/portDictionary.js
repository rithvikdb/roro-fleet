export function mergePorts(localPorts, remotePorts) {
  const byCode = new Map();
  (localPorts || []).concat(remotePorts || []).forEach((port) => {
    const normalized = normalizePort(port);
    if (normalized.code && normalized.name) byCode.set(normalized.code, normalized);
  });
  return Array.from(byCode.values()).sort((a, b) => a.name.localeCompare(b.name));
}

export function allPorts(remotePorts) {
  return mergePorts(PORT_DICTIONARY, remotePorts || []);
}

export function uniquePortCode(baseCode, ports) {
  const cleanBase = (baseCode || 'PRT').replace(/[^a-z0-9]/gi, '').slice(0, 6).toUpperCase() || 'PRT';
  const existing = new Set((ports || []).map((port) => port.code));
  if (!existing.has(cleanBase)) return cleanBase;
  for (let i = 2; i < 100; i += 1) {
    const suffix = String(i);
    const code = `${cleanBase.slice(0, Math.max(1, 6 - suffix.length))}${suffix}`;
    if (!existing.has(code)) return code;
  }
  return `${cleanBase.slice(0, 3)}${Date.now().toString().slice(-3)}`;
}

export async function lookupPortOnline(query, existingPorts) {
  const name = String(query || '').trim();
  if (!name) throw new Error('Enter a port name first.');

  const url = 'https://nominatim.openstreetmap.org/search?format=json&addressdetails=1&extratags=1&limit=5&q='
    + encodeURIComponent(`port ${name}`);
  const response = await fetch(url, { headers: { Accept: 'application/json' } });
  if (!response.ok) throw new Error(`Online lookup failed (${response.status}).`);

  const results = await response.json();
  const place = (Array.isArray(results) ? results : []).find((item) => {
    const type = String(item.type || '').toLowerCase();
    const klass = String(item.class || '').toLowerCase();
    const display = String(item.display_name || '').toLowerCase();
    return type.includes('port') || klass.includes('harbour') || display.includes('port');
  }) || (Array.isArray(results) ? results[0] : null);

  if (!place) throw new Error('No online port match found.');

  const address = place.address || {};
  const country = String(address.country_code || '').toUpperCase() || '--';
  const displayParts = String(place.display_name || name).split(',').map((part) => part.trim()).filter(Boolean);
  const foundName = displayParts[0] || name;
  const generatedCode = foundName.replace(/^(port|harbour|terminal)\s+of\s+/i, '').replace(/[^a-z0-9]/gi, '').slice(0, 3).toUpperCase().padEnd(3, 'X');
  const lon = Number(place.lon);
  const roughUtc = Number.isFinite(lon) ? Math.max(-12, Math.min(14, Math.round(lon / 15))) : 0;

  return normalizePort({
    code: uniquePortCode(generatedCode, existingPorts || PORT_DICTIONARY),
    name: foundName,
    country,
    region: address.state || address.county || address.country || 'Online lookup',
    terminal: displayParts.slice(0, 3).join(', ') || foundName,
    utc: COUNTRY_UTC[country] ?? roughUtc,
    timeZone: inferTimeZone('', country, COUNTRY_UTC[country] ?? roughUtc),
    lat: Number(place.lat),
    lon,
    custom: true,
    source: 'OpenStreetMap Nominatim',
  });
}

export function normalizePort(port) {
  const code = String(port?.code || '').toUpperCase();
  const country = String(port?.country || '--').toUpperCase();
  const utc = Number.isFinite(Number(port?.utc)) ? Number(port.utc) : 0;
  return {
    code,
    name: String(port?.name || ''),
    country,
    region: port?.region || '',
    terminal: port?.terminal || port?.name || '',
    utc,
    timeZone: port?.timeZone || inferTimeZone(code, country, utc),
    custom: port?.custom === true,
    lat: Number.isFinite(Number(port?.lat)) ? Number(port.lat) : undefined,
    lon: Number.isFinite(Number(port?.lon)) ? Number(port.lon) : undefined,
    source: port?.source,
  };
}
