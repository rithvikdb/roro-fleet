function inferTimeZone(code, country, utc) {
  if (PORT_TIME_ZONES[code]) return PORT_TIME_ZONES[code];
  const offset = Number(utc);
  if (country === 'US') {
    if (offset <= -8) return 'America/Los_Angeles';
    if (offset === -7) return 'America/Denver';
    if (offset === -6) return 'America/Chicago';
    return 'America/New_York';
  }
  if (country === 'CA') {
    if (offset <= -8) return 'America/Vancouver';
    if (offset === -6) return 'America/Winnipeg';
    return 'America/Toronto';
  }
  if (country === 'AU') {
    if (offset <= 8) return 'Australia/Perth';
    if (offset === 9.5) return 'Australia/Adelaide';
    if (offset === 10) return 'Australia/Sydney';
  }
  if (country === 'MX') {
    if (offset <= -8) return 'America/Tijuana';
    if (offset === -7) return 'America/Hermosillo';
    return 'America/Mexico_City';
  }
  return COUNTRY_TIME_ZONES[country] || '';
}

